/*  기존 테이블 삭제  */
DROP TABLE USER_HISTORY;
DROP SEQUENCE USER_HISTORY_SEQ;

/* 테이블 생성 */
CREATE TABLE USER_HISTORY
(	"ID" NUMBER(19,0) NOT NULL ENABLE,
     "USERNAME" NVARCHAR2(255),
     "ACCESSIP" NVARCHAR2(255),
     "REQUEST_URI" NVARCHAR2(255),
     "EVENT_NAME" NVARCHAR2(255),
     "EVENT_TIME" NUMBER(19,0),
     "EVENT_TIME_KR" NVARCHAR2(14),
     "USER_AGENT" NVARCHAR2(255),
     PRIMARY KEY ("ID")
)
;

/* 시퀀스 생성 */
CREATE SEQUENCE USER_HISTORY_SEQ  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 21 CACHE 20 NOORDER  NOCYCLE ;

